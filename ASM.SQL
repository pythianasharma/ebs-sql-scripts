SELECT NAME,STATE,ROUND(TOTAL_MB/(1024)) TOTAL_GB ,ROUND((TOTAL_MB-FREE_MB)/(1024)) USED_GB,ROUND(FREE_MB/(1024)) FREE_GB,
ROUND(FREE_MB/TOTAL_MB * 100) FREE_PCT
FROM V$ASM_DISKGROUP
;

SELECT DG.NAME DISKGROUP,D.NAME DISK,D.PATH,D.MOUNT_STATUS,D.HEADER_STATUS,D.OS_MB,D.TOTAL_MB,D.FREE_MB,D.TOTAL_MB-D.FREE_MB USED_MB
   FROM V$ASM_DISK D,V$ASM_DISKGROUP DG
   WHERE
   D.GROUP_NUMBER=DG.GROUP_NUMBER
   ORDER by 2
   ;

